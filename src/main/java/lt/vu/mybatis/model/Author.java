package lt.vu.mybatis.model;

public class Author {
    private Integer id;
    private String name;

    // Getters and setters for each field

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
